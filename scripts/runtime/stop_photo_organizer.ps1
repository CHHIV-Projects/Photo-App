<#
.SYNOPSIS
Stop Photo Organizer services cleanly.

.DESCRIPTION
Gracefully stops all Photo Organizer services:
- Frontend process
- Backend process
- Docker services (PostgreSQL, Redis)

.EXAMPLE
.\stop_photo_organizer.ps1

.NOTES
This is a v1.0 baseline script.
#>

param(
    [int]$GracefulTimeoutSeconds = 10
)

# ==============================================================================
# CONFIGURATION
# ==============================================================================

$ProjectRoot = Split-Path -Parent (Split-Path -Parent (Split-Path -Parent $PSScriptRoot))
$LogsDir = Join-Path $ProjectRoot "storage" "logs" "runtime"
$LogFile = Join-Path $LogsDir "shutdown_$(Get-Date -Format 'yyyyMMdd_HHmmss').log"

# ==============================================================================
# HELPER FUNCTIONS
# ==============================================================================

function Write-Log {
    param($Message)
    $line = "[$((Get-Date).ToString('HH:mm:ss'))] $Message"
    Write-Host $line
    Add-Content -Path $LogFile -Value $line -ErrorAction SilentlyContinue
}

function Write-LogSection {
    param($Title)
    $line = "--- $Title ---"
    Write-Host "`n$line" -ForegroundColor Cyan
    Add-Content -Path $LogFile -Value "`n$line" -ErrorAction SilentlyContinue
}

# ==============================================================================
# MAIN SCRIPT
# ==============================================================================

Write-Host "`n????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????`n" -ForegroundColor Cyan
Write-Host "  Photo Organizer Shutdown`n" -ForegroundColor Cyan
Write-Host "????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????`n" -ForegroundColor Cyan

# Create logs directory if needed
if (-not (Test-Path $LogsDir)) {
    New-Item -ItemType Directory -Path $LogsDir -Force | Out-Null
}

Write-LogSection "Shutdown Sequence"

# Stop frontend (npm process)
Write-Log "Stopping frontend..."
$npmProcess = Get-Process -Name "node" -ErrorAction SilentlyContinue | Where-Object { $_.CommandLine -match "next" }
if ($npmProcess) {
    Stop-Process -InputObject $npmProcess -Force -ErrorAction SilentlyContinue
    Write-Log "??? Frontend stopped"
} else {
    Write-Log "  (Frontend already stopped)"
}

Start-Sleep -Seconds 1

# Stop backend (uvicorn/python process)
Write-Log "Stopping backend..."
$backendProcess = Get-Process -Name "python" -ErrorAction SilentlyContinue | Where-Object { $_.CommandLine -match "uvicorn" }
if ($backendProcess) {
    Stop-Process -InputObject $backendProcess -Force -ErrorAction SilentlyContinue
    Write-Log "??? Backend stopped"
} else {
    Write-Log "  (Backend already stopped)"
}

Start-Sleep -Seconds 1

# Stop Docker services
Write-Log "Stopping Docker services..."
try {
    # Try to stop docker-compose services
    $DockerDir = Join-Path $ProjectRoot "docker"
    if (Test-Path $DockerDir) {
        Push-Location $DockerDir
        try {
            # Send Ctrl+C to gracefully stop
            docker-compose down 2>&1 | Out-Null
            Write-Log "??? Docker services stopped"
        } catch {
            Write-Log "  (Docker services may already be stopped)"
        } finally {
            Pop-Location
        }
    }
} catch {
    Write-Log "  (Could not stop Docker services: $_)"
}

Write-LogSection "Shutdown Complete"
Write-Log "??? All services stopped"

Write-Host "`n????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????`n" -ForegroundColor Cyan
Write-Log "Shutdown log written to: $LogFile"
Write-Host "Photo Organizer has been stopped.`n" -ForegroundColor Green

